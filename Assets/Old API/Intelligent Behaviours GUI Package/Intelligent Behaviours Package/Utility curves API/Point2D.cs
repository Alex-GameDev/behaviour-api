using System;
using System.Collections.Generic;
using System.Linq;

public class Point2D
{
    public float x, y;

    /// <summary>
    /// Creates a 2D point
    /// </summary>
    /// <param name="x">x parameter</param>
    /// <param name="y">y parameter (used as utility)</param>
    public Point2D(float x, float y)
    {
        this.x = x;
        this.y = y;
    }
}
